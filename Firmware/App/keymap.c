// 键位映射表。将物理按键序号映射为具体的HID键码（Usage ID），方便后期自定义。

#include "keymap.h"

// ============================================================
// 键位映射表：将物理按键序号（0~9）映射为 HID 键盘 Usage ID
// 修改此数组即可改变按键功能，无需改动其他代码
// 常用键码速查：
//   A:0x04, B:0x05, C:0x06, D:0x07, E:0x08, F:0x09,
//   G:0x0A, H:0x0B, I:0x0C, J:0x0D,
//   数字1:0x1E, 2:0x1F, 3:0x20, 4:0x21, 5:0x22,
//   6:0x23, 7:0x24, 8:0x25, 9:0x26, 0:0x27,
//   回车:0x28, ESC:0x29, Backspace:0x2A, Tab:0x2B, 空格:0x2C,
//   方向键上:0x52, 下:0x51, 左:0x50, 右:0x4F
// ============================================================
static const uint8_t s_keyMap[KEYMAP_SIZE] = {
    0x04,   // 物理按键 0 -> A
    0x05,   // 物理按键 1 -> B
    0x06,   // 物理按键 2 -> C
    0x07,   // 物理按键 3 -> D
    0x08,   // 物理按键 4 -> E
    0x09,   // 物理按键 5 -> F
    0x0A,   // 物理按键 6 -> G
    0x0B,   // 物理按键 7 -> H
    0x0C,   // 物理按键 8 -> I
    0x0D    // 物理按键 9 -> J
};

// ============================================================
// 实现获取键码函数（带边界检查）
// ============================================================
uint8_t KeyMap_GetCode(uint8_t index)
{
    if (index < KEYMAP_SIZE) {
        return s_keyMap[index];
    }
    return 0x00;   // 无效索引返回 0（无键）
}